            public void populateVehiclesTable(String city)
    {
        ArrayList<Vehicle> vehicleForCty = system.getVehicleCompany().vehiclesForACity(city);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        
        for(Vehicle vehicle : vehicleForCty)
        {
            Object[] row = new Object[4];
            row[0] = vehicle.getName();
            row[3] = vehicle.getLocation();
            row[1] = vehicle.getCategory();
            row[2] = vehicle.getAvailability();

            model.addRow(row);  
            
        }        
        
        
    }

