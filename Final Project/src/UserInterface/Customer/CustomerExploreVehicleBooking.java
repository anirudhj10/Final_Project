        public void calculateFare() {
        try {
            // Parse the hourly rate and number of hours entered by the user
            float hourlyRate = Float.parseFloat(jTextField4.getText());
            int numberOfHours = Integer.parseInt(jTextField5.getText());

            // Calculate the total fare
            float totalFare = hourlyRate * numberOfHours;

            // Display the total fare
            jTextField6.setText(String.format("$%.2f", totalFare));
        } catch (NumberFormatException e) {
            // Handle exception if the user does not enter a number
            jTextField6.setText("Invalid input");
        }
    }    
        
        
    
