    public void populateTable(String city)
    {
        ArrayList<Shop> shopForCty = system.getShopDirectory().shopsForACity(city);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        
        for(Shop shop : shopForCty )
        {
            Object[] row = new Object[2];
            row[0] = shop.getName();
            row[1] = shop.getLocation();

            model.addRow(row);  
            
        }        
        
        
    }
    

