    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow < 0)
            JOptionPane.showMessageDialog(this, "Please select a row to proceed!");
        else
        {
            String shopName = jTable1.getValueAt(selectedRow, 0).toString();
            Shop shop = system.getShopDirectory().getCompanyByName(shopName);
            SpecificShopJPanel panel = new SpecificShopJPanel(system, shop);
            CustomerExploreShoppingJPanel.jSplitPane1.setRightComponent(panel);
            
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

